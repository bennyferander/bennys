<template>
  <div>
    <nav>
      <button @click="currentView = 'add'">L채gg till kund</button>
      <button @click="currentView = 'list'">Visa kundlista</button>
    </nav>

    <div v-if="currentView === 'add'">
      <AddCustomer @customer-added="updateList" />
    </div>

    <div v-else>
      <CustomerList ref="customerList" />
    </div>
  </div>
</template>

<script>
import AddCustomer from './components/AddCustomer.vue'
import CustomerList from './components/CustomerList.vue'

export default {
  components: { AddCustomer, CustomerList },
  data() {
    return {
      currentView: 'add' // standardvy = formul채ret
    }
  },
  methods: {
    updateList() {
      this.$refs.customerList.refreshList()
      this.currentView = 'list' // v채xla automatiskt till listan efter till채gg
    }
  }
}
</script>

